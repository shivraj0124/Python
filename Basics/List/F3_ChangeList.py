# we can change the values present in the list using there index
thislist = ["apple", "banana", "cherry"]
thislist[1] = "blackcurrant"
print(thislist)


# we can change the values in particular range of index
thislist = ["apple", "banana", "cherry", "orange", "kiwi", "mango"]
thislist[1:3] = ["Grape", "watermelon"]
print(thislist)
