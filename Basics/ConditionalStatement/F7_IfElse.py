a = int(input("Enter a number: "))
print("Entered number is:", a)

# Conditional operators 
# Equals: a == b
# Not Equals: a != b
# Less than: a < b
# Less than or equal to: a <= b
# Greater than: a > b
# Greater than or equal to: a >= b

# if condition will true then if block will execute, Output will 'Yes' 
if(a > 20):
  print("Yes")
else:
  print("No")

# if a > 20 condition get false then else block will execute, Output will 'No'
